TTA_RUNS = 3

# 5. NIfTI 파일 찾기
nii_files = glob.glob(os.path.join(input_dir, '*', 'anat', '*.nii*'))
print("Found NIfTI files:", len(nii_files))

"""## 2) 데이터 predicted age & loss 추출"""

# 이미지 하나
for file_path in nii_files:
    print(f"\n▶ Processing: {file_path}")
    filename = os.path.basename(file_path)

    # — CSV에서 subject ID 뽑아서 나이 찾기 —
    m = re.search(r'(sub-\d+)', file_path)
    if not m:
        print("❗ subject ID (sub-XXXXX) 못 찾음")
        continue
    subj = m.group(1)
    age = age_map.get(subj)
    if age is None:
        print(f"❗ CSV에 {subj} 나이가 없음")
        continue
    print(f"Subject: {subj}, True age: {age}")

    try:

        # 6. 이미지 로드 및 전처리
        img = nib.load(file_path)
        data = img.get_fdata()


        # --- (1) 중앙 crop to (160,192,160) ---
        data = data/data.mean()
        data = dpu.crop_center(data, (160, 192, 160))


        # --- (2) Z-score 정규화 ---
        mean = data.mean()
        std = data.std()
        data = (data - mean) / std


        tta_preds = []
        for _ in range(TTA_RUNS):
            # --- (2.5) 데이터 증강 ---
            aug_data = voxel_shift(data, max_shift=3)
            aug_data = random_mirror(aug_data)

            # --- (3) Tensor 변환 및 차원 추가 ---
            input_data = torch.tensor(aug_data, dtype=torch.float32).unsqueeze(0).unsqueeze(0)
            input_data = input_data.to(device)


            # 7. 소프트 라벨 & 예측나이
            mc_preds = []
            model.train()
            with torch.no_grad():
                for _ in range(MC_DROPOUT_RUNS):
                    output = model(input_data)[0].view(-1)
                    probs = torch.exp(output).cpu().numpy()
                    bin_probs = aggregate_probs_to_bins(probs)
                    pred = np.sum(bin_probs * bin_means)
                    mc_preds.append(pred)
            tta_preds.append(np.mean(mc_preds))

        final_pred = np.mean(tta_preds)

        # 8. 예측 & 손실 계산
        with torch.no_grad():
            output = model(input_data)
        x    = output[0].view(1, -1)
        y = torch.tensor(pred, dtype=torch.float32).unsqueeze(0).to(device)
        loss = dpl.my_KLDivLoss(x, y).cpu().item()


        # 10. 결과 기록
        results.append({
            'subject': subj,
            'filename': filename,
            'true_age': age,
            'predicted_age': round(pred, 4),
            'loss': round(loss, 4)
        })


    except Exception as e:
        print(f"❌ Error on {filename}:", e)
        continue

