"""## 3) Histogram Aware Calibration"""

if results:
    df = pd.DataFrame(results)

    # Post-hoc bias correction
    print("\n🧠 Applying post-hoc bias correction...")
    y_true = df['true_age'].values.reshape(-1, 1)
    y_pred = df['predicted_age'].values.reshape(-1, 1)

    # 1. 나이대별 가중치 계산 (역비례 가중치)
    # Histogram-Aware Calibration (재샘플링 가중치 적용)
    age_counts = Counter(df['true_age'])
    df['age_weight'] = df['true_age'].map(lambda x: 1.0 / age_counts[x])
    sample_weights = df['age_weight'].values


    # Weighted linear regression
    reg = LinearRegression().fit(y_pred, y_true, sample_weight=sample_weights)
    corrected_pred = reg.predict(y_pred).flatten()
    df['corrected_age'] = np.round(corrected_pred, 4)

    y_pred_final = df['corrected_age'].values.reshape(-1, 1)


    # MAE와 R^2 계산
    mae_before = mean_absolute_error(y_true, y_pred)
    mae_after  = mean_absolute_error(y_true, y_pred_final)
    r2_before  = r2_score(y_true, y_pred)
    r2_after   = r2_score(y_true, y_pred_final)

    print(f"\n📊 MAE Before Correction : {mae_before:.2f}")
    print(f"📊 MAE After Correction  : {mae_after:.2f}")
    print(f"📈 R² Before Correction  : {r2_before:.4f}")
    print(f"📈 R² After Correction   : {r2_after:.4f}")


    summary_row = {
            'subject': 'SUMMARY',
            'filename': '',
            'true_age': '',
            'predicted_age': f'MAE: {mae_before:.2f}, R²: {r2_before:.4f}',
            'loss': '',
            'corrected_age': f'MAE: {mae_after:.2f}, R²: {r2_after:.4f}'
            }
    df = pd.concat([df, pd.DataFrame([summary_row])], ignore_index=True)

    out_csv = os.path.join(output_dir, 'brain_age_predictions.csv')
    df.to_csv(out_csv, index=False)
    print(f"\n📁 Saved all results to: {out_csv}")


