# voxel shifting (agumentation)
def voxel_shift(img, max_shift=2):
    """
    img: 3D numpy array or tensor (shape: [D, H, W])
    max_shift: 최대 이동할 voxel 수
    """
    if isinstance(img, torch.Tensor):
        img = img.numpy()

    shift_values = [random.randint(-max_shift, max_shift) for _ in range(3)]
    shifted_img = np.roll(img, shift_values[0], axis=0)  # shift along depth
    shifted_img = np.roll(shifted_img, shift_values[1], axis=1)  # shift height
    shifted_img = np.roll(shifted_img, shift_values[2], axis=2)  # shift width
    return torch.tensor(shifted_img.copy())


# mirroring (augmentation)
def random_mirror(img):
    """
    img: 3D tensor (shape: [D, H, W])
    """
    if random.random() < 0.5:
        # 좌우 뒤집기 (W axis, i.e., last axis)
        img = torch.flip(img, dims=[2])
    return img


def aggregate_probs_to_bins(probs):
    bin_probs = []
    for start, end in bin_ranges:
        indices = [i for i in range(40) if 40+i >= start and 40+i < end]
        bin_prob = probs[indices].sum()
        bin_probs.append(bin_prob)
    bin_probs = np.array(bin_probs)
    bin_probs /= bin_probs.sum()
    return bin_probs

# ── 드라이브 경로 지정 ─────────────────────────
base_dir   = '/content/drive/MyDrive/2025-1학기 Co-Deep/SFCN'
csv_path   = os.path.join(base_dir, 'Participant_LEMON_Mean_Age.csv')
model_path = os.path.join(base_dir, 'brain_age', 'run_20190719_00_epoch_best_mae.p')
